    },
    created(){

    },
    mounted(){
      this.user.name=Cookies.get('name');
      this.user.type=Cookies.get('type');
      switch(this.user.type){
        case 'teacher_info':
          this.user.role='教师';
          break;
        case 'student_info':
          this.user.role='学生';
          break;
        case 'admin_info':
          this.user.role='管理员';
          break;
      }
      var _this=this;
      $.ajax({
        type: 'get',
        url: "/hsc/student/getPersonalInfo",
        dataType: 'json',
        timeout: 60000,
        success: function(data) {
          if(data.status==='success'){
            _this.data=data.result;
          }
        },
        error: function(error) {
           
        }
      }); 
    },
    components: {
      VLink,
      'dialog-components':Dialog
    },
    methods:{
      dialogShow(msg){
        this.dialog_show=true;
        this.msg=msg;
        var _this=this;
        setTimeout(function(){
          _this.dialog_show=false;
        },1500)
      },
      logout(){
        $.ajax({
          type: 'get',
          url: "/hsc/common/loginOut",
          dataType: 'json',
          timeout: 60000,
          success: function(data) {
            if(data.status === 'success') {
              Cookies.clear('number');
              Cookies.clear('type');
              Cookies.clear('name');
              window.location.href='/';
            } else {
              
            }
          },
          error: function(error) {
             
          }
        });       
      },
      clearMessage(){
        this.alert=false;
        this.message='';
      }, 
      showMessage(msg,warning){
        this.message=msg;
        this.warning=warning;
        this.alert=true;
      },
      postPassword() {
          var _this = this;
          var origin = this.password.origin,
            newPassword = this.password.newPassword,
            again = this.password.again,
            num = /[0-9]+/,
            lowercase = /[a-z]+/,
            capital = /[A-Z]+/;
          if((!origin) || (!newPassword) || (!again)) {
            this.showMessage('信息未填写完成',true);
          } else if(newPassword.length < 6 || newPassword.length > 20) {
            this.showMessage('密码长度不正确',true);
          } else if(newPassword !== again) {
            this.showMessage('两次密码不一致',true);
          } else{
            var changePassword = {};
            changePassword.oldpasswd = this.password.origin;
            changePassword.newpasswd = this.password.newPassword;
            changePassword.renewpasswd = this.password.again;
            $.ajax({
              type: 'post',
              data: changePassword,
              url: "/hsc/student/changePassword",
              dataType: 'json',
              timeout: 60000,
              success: function(data) {
                if(data.status === 'success') {
                  $('#changePassword').modal('hide');
                  _this.dialogShow('修改成功');
                  setTimeout(function(){
                    window.location.reload();
                  },2000);
                } else {
                  _this.message = '原始密码不正确';
                }
              },
              error: function(error) {
                 _this.message = '修改失败，请稍后重试';
              }
            }); 
          } 

        },
        changeData(){
          for(var item in this.data){
            if(!this.data[item]){
              this.showMessage('信息未填写完成',true);
              return false;
            }
          }
          var _this=this;
          var data={data:this.data};
          $.ajax({
            type: 'post',
            data:data,
            url: "/hsc/student/updatePersonalInfo",
            dataType: 'json',
            timeout: 60000,
            success: function(data) {
              if(data.status==='success'){
                $('#myModal').modal('hide');
                _this.dialogShow('修改成功');
                setTimeout(function(){
                  window.location.reload();
                },2000);
              }
            },
            error: function(error) {
               
            }
          });
        }
    },
    props:['index','student','arrage','statistics','page']
  }
